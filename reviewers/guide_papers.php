 <div class="span12" style="text-align: center" id="examples"><h2 class="section">Examples of Good Reviews</h2></div>
</div>
<div class="row-fluid">
 <div class="span12">
<p>To help reviewers better understand the qualities of good, useful reviews, here are several example comments, organized by review category:</p>
<ul>
 <li><strong>The paper presentation (organization and writing style)</strong>
 <ul>
  <li><em>The organization of the units on forensics was well done.  However, the discussion of how it fits into the curriculum is overly broad and not too realistic.  Many factors were overlooked on the "curriculum side".</em></li>
 <li><em>Good level of detail on your approach. Table 2 is very handy.  Under Section 2, it seems like log analysis and auditing may fit in your column two.   How will you ensure additional security emphasis is implemented?</em></li>
 <li><em>The paper was easy to read, although it could benefit from a review of the English sentence structure.  The paper was organized in an easy-to-follow manner.  The authors explained their motivations and methods for their study.</em></li>
 <li><em>The paper could use additional proofing and polishing.  I suggest finding a non-robotics person to read for both language and communication. Some sentences are poorly formed (e.g., sent. 1 of last par. in sec. 1).  Some content seems misplaced (e.g., discussion of mobility in section 3).</em></li>
 <li><em>The organization is faultless. It is very clear what the paper is going to say and how. The paper follows through with crystal clear subject headings and a logical flow of information. There are some grammatical problems; these are not serious, but a thorough proof-reading would be helpful.</em></li>
 </ul></li>
 <li><strong>The idea or work presented is original and appropriately builds on and acknowledges previous work in the area</strong>
 <ul>
  <li><em>I would have liked to see some discussion and references setting this work in the context of other studies of student learning and knowledge retention. While I don't know of other studies that have examined exactly the phenomenon this paper does, a short search in the ACM digital library turned up these examples that are relevant...</em></li>
 </ul></li>
 <li><strong>The paper is technically sound; it includes evidence for any statements or conclusions that it makes</strong>
 <ul>
  <li><em>This paper makes a very good argument in the introduction for why this course is needed. It is timely, and addresses a topic outside of the norm often seen at SIGCSE.</em></li>
  <li><em>I can't recall ever seeing something similar at SIGCSE.  In spite of the previous problems, I would urge acceptance of this paper on a topic that we rarely see at SIGCSE. </em></li>
  <li><em>The hypotheses are too obvious and the validation of them is not enough. Therefore, the contribution of this paper is quite limited. </em></li>
 </ul></li>
 <li><strong>The idea or work presented has potential to contribute to CS education</strong>
 <ul>
  <li><em>This paper should generate a lot of discussion and have a good audience. It is a topic that many schools are trying to address (including mine.)</em></li>
  <li><em>Hard to judge given the writing organization problems, but I do not see a lot of significance here.  The verification that the laboratory helped more than on-line component alone is a nice result, if it is supported by the data. Having taught this course already and collected feedback on your approach makes the paper stronger.</em></li>
  <li><em> It is important for those who might be considering this approach to know that it can be successful.  If I were considering this approach I would want to know if the students could understand the code, and how deeply I could get into the material given time constraints.</em></li>
 </ul></li>
 <li><strong>Overall evaluation</strong>
 <ul>
  <li><em>As noted above, I'd urge acceptance of this paper because it's relevant and unique. If it had competitors, I wouldn't be nearly so kind. But I haven't seen competitors.</em></li>
  <li><em>This is a good, interesting topic, accessible to the SIGCSE audience, and widely useful.</em></li>
  <li><em>A good practical beginning guide to implementing lab exercises in a visualization course.</em></li>
  <li><em>Given the potential interest in this topic the authors could do better to capture the imagination of the reader; perhaps with a paragraph or two on famous cases.</em></li>
 </ul></li>
 <li><strong>Presentation</strong>
 <ul>
  <li><em>The material stands well on its own but could be made more attractive for a presentation. Help the audience understand why this is interesting.  Relate some &quot;exciting horror stories&quot; of real-world failures. Justify why a full semester should be devoted to this course in an already tightly-packed curriculum.</em></li>
  <li><em>After introducing the course, spend time on how you handle the diverse population. Also spend time on the lab components that the students found most interesting. Provide handouts of the lab exercises for those who are interested (or provide a link to a web resource.)</em></li>
 </ul></li>
</ul>
