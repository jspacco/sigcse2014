<div class="row-fluid">
 <div class="span12" style="text-align: center" id="suggestions"><h2 class="section">More Detailed Suggestions</h2></div>
</div>
<div class="row-fluid">
 <div class="span12">
<p>Some examples of information that should be relayed through the review process for panels:
<ul>
<li>
Summary of Submission:
  <ul>
  <li>Please summarize the submission in 2-4 sentences in your own words. Please DO NOT copy/paste the abstract into this section.</li>
  </ul>
</li>
<li>
Strengths of this Submission (Some sample questions to guide your responses in this section):
  <ul>
  <li>Is the panel topic suitable for the symposium?</li>
  <li>Do you expect that the level of interest in the panel would be high?</li>
  <li>Are the particular presenters appropriate? Does their expertise match the panel topic? (Remember that Panel reviewing is not blind.)</li>
  <li>Does the proposal appropriately place the topic in the larger context of Computer Science education? Are the authors aware of a range of ideas on the topic? If appropriate, do the authors cite related work? (In evaluating this, please keep in mind that proposals are limited to two pages, so it is unreasonable to expect a detailed "Related Work" section or a long bibliography.)</li>
  <li>Is the structure of the panel presentation reasonable? </li>
  <li>Is the topic a good one for a panel? Is there some other format that would be more appropriate (e.g., a special session, workshop or paper)?</li>
  <li>Will the speakers represent multiple views and offer distinct ideas?</li>
  <li>Will the speakers be able to present their views in the time allotted (75 minutes total), while leaving time for questions or audience participation (30 minutes suggested)?</li>
  <li>A typical panel consists of 4 participants including the moderator. If there are more than 4, have they convinced you that everyone will be able to speak and that time will still be available for audience participation?</li>
  </ul>
</li>
<li>
Comments for Authors/Areas for Improvement 
  <ul>
  <li>Is the proposal well-written? Is it clear and well-organized? Are there any technical errors?</li>
  <li>Do you have any suggestions for the authors ... </li>
    <ul>
      <li>...to improve the proposal itself (either for publication in the proceedings or for submission to a future symposium)?</li>
      <li>...to improve the quality of the presentation, if accepted?</li>
    </ul>
  </ul>
</li>
</ul>
Obviously this list is not exhaustive. The Program Committee and authors will appreciate your views on other issues as well.
</p>


