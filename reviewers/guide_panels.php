 <div class="span12" style="text-align: center" id="guidelines"><h2 class="section">Guidelines for Reviewers of Panels</h2></div>
</div>
<div class="row-fluid">
 <div class="span12">
<p>Your job as a reviewer of Panels is an important one. Through your reviews you have the opportunity to help the authors and to improve the quality of the symposium. Below are some general guidelines for writing good Panel reviews:</p>
<ul>
 <li>Your job is to write detailed reviews, even for excellent proposals. Tell the authors why you liked their proposal, so that they know what made it successful.</li>
 <li>If you believe that the proposal is poorly written or poorly thought-out, provide constructive criticism to help the authors.</li>
 <li>The best reviews clearly justify the reviewer's choice of rating. The least valuable review gives a low score with no written comments. This simply tells the authors that they have been unsuccessful, with no indication of how or why. It is of no help to the members of the Program Committee, who are charged with making program decisions based on your reviews.</li>
 <li>Although SIGCSE requires all Panel proposals to be polished work, the authors will have a brief opportunity to improve their proposals before camera-ready copy is due. Your detailed feedback may help improve a panel and, as a result, the conference.</li>
 <li><strong>DISTINCTION BETWEEN PANELS AND SPECIAL SESSIONS:</strong> Panels present multiple perspectives on a specific topic. Special sessions are an opportunity to customize and experiment with the SIGCSE conference format (e.g. a seminar on a new topic, a committee report, or a forum on curriculum issues)</li>

</li>     
</ul>
 </div>
</div>

