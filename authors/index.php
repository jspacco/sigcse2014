 <div class="span12" style="text-align: right;">
    <h2>CALL FOR PARTICIPATION</h2>
    <h1>SIGCSE 2014</h1>
    <h3>Leveraging Computing to Change Education<br/>
    The 45th ACM Technical Symposium on<br/>Computer Science Education</h3>
    <p>March 5 – 8, 2014, Atlanta, Georgia, USA<br/>
    <em><a href="http://sigcse2014.sigcse.org/">http://sigcse2014.sigcse.org/</a></em></p>
 </div>
