<div class="row-fluid">
  <div class="span12">
    <p>SIGCSE 2014 continues our long tradition of bringing together colleagues from around the world to present papers, panels, posters, special sessions, and workshops, and to discuss computer science education in birds-of-a-feather sessions and informal settings. The SIGCSE Technical Symposium addresses problems common among educators working to develop, implement and/or evaluate computing programs, curricula, and courses. The symposium provides a forum for sharing new ideas for syllabi, laboratories, and other elements of teaching and pedagogy, at all levels of instruction.</p>
    <p>Submissions in line with the conference theme, “Leveraging Computing to Change Education,” are encouraged. The theme focuses our attention on how computing influences the way we educate at all levels. This influence includes applications designed to support student learning, the curricular impact of computing, learning research, and the impact of technology in all forms of education. We want to look beyond the conventional computer science education boundaries for connections as well.</p>
    <p id="papers"><strong><a href="papers.php">PAPERS</a></strong><br/>
    Papers describe an educational research project, classroom experience, teaching technique, curricular initiative, or pedagogical tool. Two versions of a submission are required: a full version having author names and affiliations and an anonymous version for use in reviewing. Papers will undergo a blind reviewing process and must not exceed six pages. Authors will have approximately 25 minutes for their presentations, including questions and answers.</p>
    <p id="panels"><strong><a href="panels.php">PANELS</a></strong><br/>
    Panels present multiple perspectives on a specific topic. To allow each panelist sufficient time to present his or her perspective and still enable audience participation, a panel will normally have at most four panelists, including one moderator. Panel submissions should include a list of the panelists, their affiliations, and a description of the topic, with brief position statements from panelists. Proposals with more than four panelists must provide a statement connecting the extra panelist to the effectiveness of the panel and must convincingly show that each panelist will be able to speak, and the audience able to respond, within the session time. Panel abstracts must not exceed two pages. A panel session is approximately 75 minutes.</p>
    <p id="specialsessions"><strong><a href="special_sessions.php">SPECIAL SESSIONS</a></strong><br/>
    Special sessions are your opportunity to customize and experiment with the SIGCSE conference format. Possible special sessions include a seminar on a new topic, a committee report, or a forum on curriculum issues. More generally, they must be 75 minutes in length, held in standard conference spaces, and justifiably distinct from the panel, paper, and poster tracks. Within those constraints, the form is yours to design. Special session abstracts must not exceed two pages.</p>
    <p id="workshops"><strong><a href="workshops.php">WORKSHOPS</a></strong><br/>
    Workshops offer participants opportunities to learn new techniques and technologies designed to foster education, scholarship, and collaborations. A workshop proposal (including abstract) must not exceed two pages. Proposals must specify equipment needs (e.g., participant-supplied laptops, room configurations, and A/V equipment) and any limitation on the number of participants. Workshops are scheduled for a three-hour session and do not conflict with the technical sessions.</p>
    <p id="bofs"><strong><a href="bof.php">BIRDS OF A FEATHER SESSIONS</a></strong><br/>
    Birds of a Feather (BOF) sessions provide an environment for colleagues with similar interests to meet for informal discussions. A maximum one-page description (including abstract) is requested to describe the informal discussion topic. A/V equipment will not be provided for these sessions. Approximately 45 minutes are allocated to each BOF topic.</p>
    <p id="posters"><strong><a href="posters.php">POSTERS</a></strong><br/>
    Posters describe computer science education materials or research, particularly works in progress. Proposals (including abstract) are limited to two pages. Poster demonstrations are scheduled to permit one-on-one discussion with conference attendees, typically during session breaks. Prepared handouts are encouraged in order to share your work.</p>
    <p id="src"><strong><a href="student_research.php">STUDENT RESEARCH COMPETITION</a></strong><br/>
    Research from all areas of computer science is considered for awards in two categories of competition: graduate and undergraduate. All submissions must represent a student's individual research contribution and a student must be an ACM student member to qualify for awards and travel grants. Entry due date is September 29, 2013.</p>
